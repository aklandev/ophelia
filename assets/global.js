document.addEventListener('DOMContentLoaded', () => {
  // Init header height variable
  (function () {
    let currentWidth = 0;
    function calcVh() {
      if (currentWidth == window.innerWidth) return;

      currentWidth = window.innerWidth;

      let vh = window.innerHeight * 0.01;
      document.documentElement.style.setProperty('--vh', `${vh}px`);
    }
    calcVh();
    window.addEventListener('resize', calcVh);
  })();

  // Theme custom cursors
  (() => {
    theme.customCursor = theme.customCursor || {};
    theme.customCursor.init = () => {
      const customCursorWrapper = document.querySelectorAll('[data-enable-custom-cursor]');

      if (customCursorWrapper.length < 1) return;

      customCursorWrapper.forEach(wrapper => {
        let cursor = wrapper.querySelector('[data-custom-cursor]');

        wrapper.addEventListener('mouseenter', () => {
          cursor.classList.remove('hidden');
        });

        wrapper.addEventListener('mousemove', (e) => {
          cursor.style.top = e.pageY + 'px';
          cursor.style.left = e.pageX + 'px';
        });

        wrapper.addEventListener('mouseleave', () => {
          cursor.classList.add('hidden');
        });
      });
    }
    theme.customCursor.init();
    document.addEventListener('shopify:section:load', () => theme.customCursor.init())
  })();
})

